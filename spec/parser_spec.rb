require_relative '../objects/parser'

describe Parser do
  subject { Parser.new }

  it 'should parse lines' do
    expect(subject.parse('Add foo 12345 $123').verb).to eq(:add)
    expect(subject.parse('Charge foo $123').verb).to eq(:charge)
    expect(subject.parse('Credit foo $123').verb).to eq(:credit)
  end
end
